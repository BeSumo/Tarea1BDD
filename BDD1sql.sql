DROP TABLE IF EXISTS Autor;

CREATE TABLE Autor (
  RUT_autor VARCHAR(15) PRIMARY KEY,
  nombre VARCHAR(255),
  email VARCHAR(255)
);

DROP TABLE IF EXISTS Revisor;

CREATE TABLE Revisor (
  RUT_revisor VARCHAR(15) PRIMARY KEY,
  nombre VARCHAR(255) NOT NULL,
  Email VARCHAR(255) NOT NULL,
  Especialidad VARCHAR(100) NOT NULL -- falta agregar especialidades
);

DROP TABLE IF EXISTS Topico;
CREATE TABLE Topico (
  id_topico INT AUTO_INCREMENT PRIMARY KEY,
  nombre_topico VARCHAR(100) NOT NULL UNIQUE,
  descripcion TEXT,
  fecha_creacion DATE DEFAULT CURRENT_DATE
);

-- falta insercion de topicos en topicos _articulo
DROP TABLE IF EXISTS Topico_Articulo;
CREATE TABLE Topico_Articulo (
  id_topico INT,
  id_articulo INT,
  PRIMARY KEY (id_topico, id_articulo),
  FOREIGN KEY (id_topico) REFERENCES Topico(id_topico) ON DELETE CASCADE,
  FOREIGN KEY (id_articulo) REFERENCES Articulo(id_Articulo) ON DELETE CASCADE
);
DROP TABLE IF EXISTS Autor_Articulo;
CREATE TABLE Autor_Articulo (
  rut_autor VARCHAR(15),
  id_articulo INT,
  orden_autor INT,
  es_contacto BOOLEAN DEFAULT FALSE,
  PRIMARY KEY (rut_autor, id_articulo),
  FOREIGN KEY (rut_autor) REFERENCES Autor(rut),
  FOREIGN KEY (id_articulo) REFERENCES Articulo(id_Articulo)
);

